package bank_test

import (
	"fmt"
	"testing"

	bank "github.com/PieerePi/gople/ch9/e9.1"
)

func TestBank(t *testing.T) {
	done := make(chan struct{})

	// Alice
	go func() {
		bank.Deposit(200)
		fmt.Println("=", bank.Balance())
		done <- struct{}{}
	}()

	// Bob
	go func() {
		bank.Deposit(100)
		done <- struct{}{}
	}()

	// Wait for both transactions.
	<-done
	<-done

	if got, want := bank.Balance(), 300; got != want {
		t.Errorf("Balance = %d, want %d", got, want)
	}

	// Carl
	go func() {
		result := bank.Withdraw(200)
		fmt.Println("withdraw 200", result)
		done <- struct{}{}
	}()

	// David
	go func() {
		result := bank.Withdraw(350)
		fmt.Println("withdraw 350", result)
		done <- struct{}{}
	}()

	// Wait for both transactions.
	<-done
	<-done

	if got, want := bank.Balance(), 100; got != want {
		t.Errorf("Balance = %d, want %d", got, want)
	}
}
